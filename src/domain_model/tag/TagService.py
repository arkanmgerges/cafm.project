from src.domain_model.role.Role import Role
from typing import List, Tuple
from src.domain_model.tag.Tag import Tag
from src.domain_model.tag.TagRepository import TagRepository
from src.domain_model.token.TokenData import TokenData
from src.resource.logging.decorator import debugLogger


class TagService:
    def __init__(self, repository: TagRepository):
        self._repo = repository

    @debugLogger
    def createTag(self, obj: Tag, objectOnly: bool = False, tokenData: TokenData = None):
        if objectOnly:
            return Tag.createFromObject(obj=obj, generateNewId=True) if obj.id() == '' else obj
        else:
            obj: Tag = Tag.createFromObject(obj=obj, publishEvent=True)
            self._repo.save(obj=obj)
            return obj

    @debugLogger
    def deleteTag(self, obj: Tag, tokenData: TokenData = None):
        obj.publishDelete()
        self._repo.deleteTag(obj=obj)

    @debugLogger
    def updateTag(self, oldObject: Tag, newObject: Tag, tokenData: TokenData = None):
        newObject.publishUpdate(oldObject)
        self._repo.save(obj=newObject)

    @debugLogger
    def bulkCreate(self, objList: List[Tag]):
        self._repo.bulkSave(objList=objList)
        for obj in objList:
            Tag.createFromObject(obj=obj, publishEvent=True)

    @debugLogger
    def bulkDelete(self, objList: List[Tag]):
        self._repo.bulkDelete(objList=objList)
        for obj in objList:
            obj.publishDelete()

    @debugLogger
    def bulkUpdate(self, objList: List[Tuple]):
        newObjList = list(map(lambda x: x[0], objList))
        self._repo.bulkSave(objList=newObjList)
        for obj in objList:
            newObj = obj[0]
            oldObj = obj[1]
            newObj.publishUpdate(oldObj)

    @debugLogger
    def assignTagToRole(
            self, role: Role, tag: Tag, tokenData: TokenData = None
        ):
        self._repo.assignTagToRole(tag=tag, role=role, tokenData=tokenData)

    @debugLogger
    def tags(self, tokenData: TokenData = None, resultFrom: int = 0, resultSize: int = 100,
                      order: List[dict] = None):
        return self._repo.tags(tokenData=tokenData, resultFrom=resultFrom, resultSize=resultSize, order=order)
